package action.game;

public class HitLine {
	public double startX;
	public double startY;
	public double endX;
	public double endY;

	public HitLine(double startX, double startY, double endX, double endY) {
		super();
		this.startX = startX;
		this.startY = startY;
		this.endX = endX;
		this.endY = endY;
	}
}
